/**
 * problem: https://leetcode.com/problems/valid-sudoku
 * date: 30.03.2024
 * @param string[][] $board
 * @return bool
 */
function isValidSudoku($board)
{
    $columnMap = [];

    for($i=0; $i<9; $i++){
        $columnMap[$i]=[];
    }

    foreach ($board as $pkey => $columns) {

        foreach ($columns as $ck => $row) {           

            if ($row !== "." && !in_array($row, $columnMap[$ck])) {
                
                $columnMap[$ck][] = $row;

            }
            elseif($row!=="."){
                
                return false;

            }
           
            $temp="";

            if($row!=="."){
                
                $temp=$row;
                
                unset($columns[$ck]);
            }

            if(in_array($temp,$columns)){
                return false;
            }

        }
    }

    return true;
}
